-- 7e. FLEET_HEALTH_DASHBOARD_V1
CREATE OR REPLACE DYNAMIC TABLE FLEET_HEALTH_DASHBOARD_V1
    TARGET_LAG = '10 minutes'
    WAREHOUSE = TML_HACKATHON
    REFRESH_MODE = AUTO
    INITIALIZE = ON_CREATE
    COMMENT = 'Fleet health overview: per-vehicle health status based on DTC counts, CAN bus health, battery, coolant, tamper alerts, and connectivity.'
AS
WITH latest_j1939 AS (
    SELECT CUSTOMER_ID, VEHICLE_ID, DEVICE_ID,
        MAX(EVENT_TS) AS LAST_EVENT_TS,
        MAX(J1939_ACTIVE_DTC_COUNT) AS MAX_DTC_COUNT,
        MAX(J1939_COOLANT_TEMP_C) AS MAX_COOLANT_TEMP_C,
        MIN(J1939_BATTERY_VOLTAGE_V) AS MIN_BATTERY_VOLTAGE_V,
        MAX(J1939_ODOMETER_KM) AS CURRENT_ODOMETER_KM,
        MAX(J1939_ENGINE_HOURS) AS CURRENT_ENGINE_HOURS
    FROM TML_FLEET_TELEMETRY.CURATED.J1939_SIGNALS_V1
    GROUP BY CUSTOMER_ID, VEHICLE_ID, DEVICE_ID
),
latest_telematics AS (
    SELECT VEHICLE_ID,
        AVG(TEL_GSM_SIGNAL_DBM) AS AVG_GSM_SIGNAL_DBM,
        MAX(TEL_STORAGE_QUEUE_DEPTH) AS MAX_QUEUE_DEPTH,
        BOOLAND_AGG(CASE WHEN TEL_CAN_BUS_HEALTH = 'HEALTHY' THEN TRUE ELSE FALSE END) AS ALL_CAN_HEALTHY,
        BOOLOR_AGG(COALESCE(TEL_TAMPER_ALERT, FALSE)) AS HAS_TAMPER_ALERT
    FROM TML_FLEET_TELEMETRY.CURATED.TELEMATICS_DEVICE_PARAMS_V1
    GROUP BY VEHICLE_ID
)
SELECT
    j.CUSTOMER_ID, j.VEHICLE_ID, j.DEVICE_ID, j.LAST_EVENT_TS,
    DATEDIFF('MINUTE', j.LAST_EVENT_TS, CURRENT_TIMESTAMP()) AS MINUTES_SINCE_LAST_EVENT,
    COALESCE(j.MAX_DTC_COUNT, 0) AS MAX_DTC_COUNT,
    NOT t.ALL_CAN_HEALTHY AS HAS_CAN_BUS_ISSUES,
    t.HAS_TAMPER_ALERT,
    ROUND(j.MAX_COOLANT_TEMP_C, 1) AS MAX_COOLANT_TEMP_C,
    ROUND(j.MIN_BATTERY_VOLTAGE_V, 1) AS MIN_BATTERY_VOLTAGE_V,
    ROUND(t.AVG_GSM_SIGNAL_DBM, 1) AS AVG_GSM_SIGNAL_DBM,
    t.MAX_QUEUE_DEPTH,
    ROUND(j.CURRENT_ODOMETER_KM, 0) AS CURRENT_ODOMETER_KM,
    ROUND(j.CURRENT_ENGINE_HOURS, 0) AS CURRENT_ENGINE_HOURS,
    CASE
        WHEN COALESCE(j.MAX_DTC_COUNT, 0) > 5 OR t.HAS_TAMPER_ALERT THEN 'CRITICAL'
        WHEN COALESCE(j.MAX_DTC_COUNT, 0) > 2 OR NOT t.ALL_CAN_HEALTHY THEN 'WARNING'
        WHEN j.MAX_COOLANT_TEMP_C > 105 OR j.MIN_BATTERY_VOLTAGE_V < 22 THEN 'ALERT'
        ELSE 'HEALTHY'
    END AS HEALTH_STATUS
FROM latest_j1939 j
LEFT JOIN latest_telematics t ON j.VEHICLE_ID = t.VEHICLE_ID;

-- 7f. TRIP_SUMMARIES_V1
CREATE OR REPLACE DYNAMIC TABLE TRIP_SUMMARIES_V1
    TARGET_LAG = '2 minutes'
    WAREHOUSE = TML_HACKATHON
    REFRESH_MODE = AUTO
    INITIALIZE = ON_CREATE
AS
WITH ignition_transitions AS (
    SELECT t.CUSTOMER_ID, t.VEHICLE_ID, t.EVENT_TS, t.TEL_IGNITION_STATUS,
        CASE WHEN t.TEL_IGNITION_STATUS = 'ON'
            AND (LAG(t.TEL_IGNITION_STATUS) OVER (PARTITION BY t.VEHICLE_ID ORDER BY t.EVENT_TS) = 'OFF'
                 OR LAG(t.TEL_IGNITION_STATUS) OVER (PARTITION BY t.VEHICLE_ID ORDER BY t.EVENT_TS) IS NULL)
            THEN 1 ELSE 0
        END AS TRIP_START_FLAG
    FROM TML_FLEET_TELEMETRY.CURATED.TELEMATICS_DEVICE_PARAMS_V1 t
),
trip_groups AS (
    SELECT *, SUM(TRIP_START_FLAG) OVER (PARTITION BY VEHICLE_ID ORDER BY EVENT_TS) AS TRIP_NUM
    FROM ignition_transitions WHERE TEL_IGNITION_STATUS = 'ON'
),
trip_boundaries AS (
    SELECT CUSTOMER_ID, VEHICLE_ID, TRIP_NUM,
        MIN(EVENT_TS) AS TRIP_START_TS, MAX(EVENT_TS) AS TRIP_END_TS
    FROM trip_groups WHERE TRIP_NUM > 0
    GROUP BY CUSTOMER_ID, VEHICLE_ID, TRIP_NUM
),
trip_gps AS (
    SELECT tb.CUSTOMER_ID, tb.VEHICLE_ID, tb.TRIP_NUM,
        tb.TRIP_START_TS, tb.TRIP_END_TS,
        TIMESTAMPDIFF('SECOND', tb.TRIP_START_TS, tb.TRIP_END_TS) AS DURATION_SEC,
        FIRST_VALUE(g.GPS_LATITUDE) OVER (PARTITION BY tb.VEHICLE_ID, tb.TRIP_NUM ORDER BY g.EVENT_TS) AS START_LAT,
        FIRST_VALUE(g.GPS_LONGITUDE) OVER (PARTITION BY tb.VEHICLE_ID, tb.TRIP_NUM ORDER BY g.EVENT_TS) AS START_LON,
        LAST_VALUE(g.GPS_LATITUDE) OVER (PARTITION BY tb.VEHICLE_ID, tb.TRIP_NUM ORDER BY g.EVENT_TS ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS END_LAT,
        LAST_VALUE(g.GPS_LONGITUDE) OVER (PARTITION BY tb.VEHICLE_ID, tb.TRIP_NUM ORDER BY g.EVENT_TS ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS END_LON,
        ROW_NUMBER() OVER (PARTITION BY tb.VEHICLE_ID, tb.TRIP_NUM ORDER BY g.EVENT_TS) AS gps_rn
    FROM trip_boundaries tb
    JOIN TML_FLEET_TELEMETRY.CURATED.GPS_SIGNALS_V1 g
        ON g.VEHICLE_ID = tb.VEHICLE_ID AND g.EVENT_TS BETWEEN tb.TRIP_START_TS AND tb.TRIP_END_TS
),
trip_j1939 AS (
    SELECT tb.VEHICLE_ID, tb.TRIP_NUM,
        MAX(j.J1939_ODOMETER_KM) - MIN(j.J1939_ODOMETER_KM) AS DISTANCE_KM,
        MAX(j.J1939_TOTAL_FUEL_USED_L) - MIN(j.J1939_TOTAL_FUEL_USED_L) AS FUEL_CONSUMED_L,
        AVG(j.J1939_VEHICLE_SPEED_KPH) AS AVG_SPEED_KPH,
        MAX(j.J1939_VEHICLE_SPEED_KPH) AS MAX_SPEED_KPH,
        AVG(j.J1939_ENGINE_RPM) AS AVG_ENGINE_RPM,
        AVG(j.J1939_ENGINE_LOAD_PCT) AS AVG_ENGINE_LOAD_PCT
    FROM trip_boundaries tb
    JOIN TML_FLEET_TELEMETRY.CURATED.J1939_SIGNALS_V1 j
        ON j.VEHICLE_ID = tb.VEHICLE_ID AND j.EVENT_TS BETWEEN tb.TRIP_START_TS AND tb.TRIP_END_TS
    GROUP BY tb.VEHICLE_ID, tb.TRIP_NUM
),
trip_geofence_start AS (
    SELECT DISTINCT tb.VEHICLE_ID, tb.TRIP_NUM, gf.CITY_NAME AS START_GEOFENCE
    FROM trip_boundaries tb
    JOIN TML_FLEET_TELEMETRY.CURATED.GPS_SIGNALS_V1 g ON g.VEHICLE_ID = tb.VEHICLE_ID AND g.EVENT_TS = tb.TRIP_START_TS
    JOIN TML_FLEET_TELEMETRY.MASTER.GEOFENCE_DEFINITIONS gf ON HAVERSINE(g.GPS_LATITUDE, g.GPS_LONGITUDE, gf.LATITUDE, gf.LONGITUDE) <= gf.RADIUS_KM
    QUALIFY ROW_NUMBER() OVER (PARTITION BY tb.VEHICLE_ID, tb.TRIP_NUM ORDER BY HAVERSINE(g.GPS_LATITUDE, g.GPS_LONGITUDE, gf.LATITUDE, gf.LONGITUDE)) = 1
),
trip_geofence_end AS (
    SELECT DISTINCT tb.VEHICLE_ID, tb.TRIP_NUM, gf.CITY_NAME AS END_GEOFENCE
    FROM trip_boundaries tb
    JOIN TML_FLEET_TELEMETRY.CURATED.GPS_SIGNALS_V1 g ON g.VEHICLE_ID = tb.VEHICLE_ID AND g.EVENT_TS = tb.TRIP_END_TS
    JOIN TML_FLEET_TELEMETRY.MASTER.GEOFENCE_DEFINITIONS gf ON HAVERSINE(g.GPS_LATITUDE, g.GPS_LONGITUDE, gf.LATITUDE, gf.LONGITUDE) <= gf.RADIUS_KM
    QUALIFY ROW_NUMBER() OVER (PARTITION BY tb.VEHICLE_ID, tb.TRIP_NUM ORDER BY HAVERSINE(g.GPS_LATITUDE, g.GPS_LONGITUDE, gf.LATITUDE, gf.LONGITUDE)) = 1
)
SELECT
    tg.CUSTOMER_ID, tg.VEHICLE_ID, tg.TRIP_NUM,
    tg.TRIP_START_TS, tg.TRIP_END_TS, tg.DURATION_SEC,
    ROUND(tg.DURATION_SEC / 60.0, 1) AS DURATION_MIN,
    tg.START_LAT, tg.START_LON, tg.END_LAT, tg.END_LON,
    ROUND(tj.DISTANCE_KM, 2) AS DISTANCE_KM,
    ROUND(tj.FUEL_CONSUMED_L, 2) AS FUEL_CONSUMED_L,
    CASE WHEN tj.DISTANCE_KM > 0 THEN ROUND(tj.FUEL_CONSUMED_L / tj.DISTANCE_KM * 100, 2) END AS FUEL_EFFICIENCY_L_PER_100KM,
    ROUND(tj.AVG_SPEED_KPH, 1) AS AVG_SPEED_KPH,
    ROUND(tj.MAX_SPEED_KPH, 1) AS MAX_SPEED_KPH,
    ROUND(tj.AVG_ENGINE_RPM, 0) AS AVG_ENGINE_RPM,
    ROUND(tj.AVG_ENGINE_LOAD_PCT, 1) AS AVG_ENGINE_LOAD_PCT,
    gs.START_GEOFENCE, ge.END_GEOFENCE
FROM trip_gps tg
LEFT JOIN trip_j1939 tj ON tg.VEHICLE_ID = tj.VEHICLE_ID AND tg.TRIP_NUM = tj.TRIP_NUM
LEFT JOIN trip_geofence_start gs ON tg.VEHICLE_ID = gs.VEHICLE_ID AND tg.TRIP_NUM = gs.TRIP_NUM
LEFT JOIN trip_geofence_end ge ON tg.VEHICLE_ID = ge.VEHICLE_ID AND tg.TRIP_NUM = ge.TRIP_NUM
WHERE tg.gps_rn = 1;

