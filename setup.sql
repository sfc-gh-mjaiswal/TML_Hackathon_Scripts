    COMMENT = 'Daily customer-specific retention enforcement. Deletions propagate through CURATED DTs to BUSINESSREADY.'
AS
CALL TML_FLEET_TELEMETRY.RAW.ENFORCE_CUSTOMER_RETENTION();

