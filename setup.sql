-- ────────────────────────────────────────────────────────────────────────────
-- STEP 1: Warehouse, Database, Schemas
-- ────────────────────────────────────────────────────────────────────────────
USE ROLE ACCOUNTADMIN;

CREATE OR REPLACE WAREHOUSE TML_HACKATHON
    WAREHOUSE_SIZE = 'LARGE'
    WAREHOUSE_TYPE = 'STANDARD'
    AUTO_SUSPEND = 300
    AUTO_RESUME = TRUE;

CREATE OR REPLACE DATABASE TML_FLEET_TELEMETRY;

USE WAREHOUSE TML_HACKATHON;

CREATE SCHEMA TML_FLEET_TELEMETRY.RAW;
CREATE SCHEMA TML_FLEET_TELEMETRY.CURATED;
CREATE SCHEMA TML_FLEET_TELEMETRY.BUSINESSREADY;
CREATE SCHEMA TML_FLEET_TELEMETRY.MASTER;

-- ────────────────────────────────────────────────────────────────────────────
-- STEP 2: Ingestion Service User
-- ────────────────────────────────────────────────────────────────────────────
-- IMPORTANT: Replace <RSA_PUBLIC_KEY_CONTENT> with the contents of rsa_key.pub
-- (excluding the BEGIN/END PUBLIC KEY lines).

CREATE USER IF NOT EXISTS TML_INGEST_USER
    TYPE = SERVICE
    RSA_PUBLIC_KEY = '<RSA_PUBLIC_KEY_CONTENT>';

CREATE ROLE IF NOT EXISTS TML_INGEST_ROLE;
GRANT ROLE TML_INGEST_ROLE TO USER TML_INGEST_USER;

GRANT USAGE ON WAREHOUSE TML_HACKATHON TO ROLE TML_INGEST_ROLE;
GRANT USAGE ON DATABASE TML_FLEET_TELEMETRY TO ROLE TML_INGEST_ROLE;
GRANT USAGE ON SCHEMA TML_FLEET_TELEMETRY.RAW TO ROLE TML_INGEST_ROLE;
GRANT INSERT, SELECT ON ALL TABLES IN SCHEMA TML_FLEET_TELEMETRY.RAW TO ROLE TML_INGEST_ROLE;
GRANT INSERT, SELECT ON FUTURE TABLES IN SCHEMA TML_FLEET_TELEMETRY.RAW TO ROLE TML_INGEST_ROLE;

