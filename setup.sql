-- 7a. FACT_VEHICLE_DAILY_METRICS_V1
CREATE OR REPLACE DYNAMIC TABLE FACT_VEHICLE_DAILY_METRICS_V1
    TARGET_LAG = '5 minutes'
    WAREHOUSE = TML_HACKATHON
    REFRESH_MODE = AUTO
    INITIALIZE = ON_CREATE
AS
SELECT
    j.CUSTOMER_ID, j.VEHICLE_ID,
    DATE_TRUNC('DAY', j.EVENT_TS) AS METRIC_DATE,
    COUNT(*) AS TOTAL_READINGS,
    ROUND(AVG(j.J1939_VEHICLE_SPEED_KPH), 1) AS AVG_SPEED_KPH,
    ROUND(MAX(j.J1939_VEHICLE_SPEED_KPH), 1) AS MAX_SPEED_KPH,
    ROUND(AVG(j.J1939_ENGINE_RPM), 0) AS AVG_ENGINE_RPM,
    ROUND(MAX(j.J1939_ENGINE_RPM), 0) AS MAX_ENGINE_RPM,
    ROUND(AVG(j.J1939_ENGINE_LOAD_PCT), 1) AS AVG_ENGINE_LOAD_PCT,
    ROUND(AVG(j.J1939_FUEL_RATE_LPH), 2) AS AVG_FUEL_RATE_LPH,
    ROUND(MAX(j.J1939_TOTAL_FUEL_USED_L) - MIN(j.J1939_TOTAL_FUEL_USED_L), 2) AS DAILY_FUEL_CONSUMED_L,
    ROUND(MAX(j.J1939_ODOMETER_KM) - MIN(j.J1939_ODOMETER_KM), 2) AS DAILY_DISTANCE_KM,
    ROUND(AVG(j.J1939_COOLANT_TEMP_C), 1) AS AVG_COOLANT_TEMP_C,
    ROUND(MAX(j.J1939_COOLANT_TEMP_C), 1) AS MAX_COOLANT_TEMP_C,
    ROUND(AVG(j.J1939_BATTERY_VOLTAGE_V), 2) AS AVG_BATTERY_V,
    ROUND(MAX(j.J1939_ENGINE_HOURS) - MIN(j.J1939_ENGINE_HOURS), 2) AS DAILY_ENGINE_HOURS,
    SUM(CASE WHEN j.J1939_BRAKE_SWITCH = TRUE THEN 1 ELSE 0 END) AS BRAKE_EVENTS,
    MAX(j.J1939_ACTIVE_DTC_COUNT) AS MAX_DTC_COUNT,
    ROUND(AVG(j.J1939_TYRE_PRESSURE_FRONT_LEFT_KPA), 1) AS AVG_TYRE_FL_KPA,
    ROUND(AVG(j.J1939_TYRE_PRESSURE_FRONT_RIGHT_KPA), 1) AS AVG_TYRE_FR_KPA,
    ROUND(AVG(j.J1939_TYRE_PRESSURE_REAR_LEFT_KPA), 1) AS AVG_TYRE_RL_KPA,
    ROUND(AVG(j.J1939_TYRE_PRESSURE_REAR_RIGHT_KPA), 1) AS AVG_TYRE_RR_KPA,
    ROUND(AVG(j.J1939_DEF_LEVEL_PCT), 1) AS AVG_DEF_LEVEL_PCT
FROM TML_FLEET_TELEMETRY.CURATED.J1939_SIGNALS_V1 j
GROUP BY j.CUSTOMER_ID, j.VEHICLE_ID, DATE_TRUNC('DAY', j.EVENT_TS);

-- 7b. FACT_CUSTOMER_FLEET_SUMMARY_V1
CREATE OR REPLACE DYNAMIC TABLE FACT_CUSTOMER_FLEET_SUMMARY_V1
    TARGET_LAG = '5 minutes'
    WAREHOUSE = TML_HACKATHON
    REFRESH_MODE = AUTO
    INITIALIZE = ON_CREATE
AS
SELECT
    j.CUSTOMER_ID,
    DATE_TRUNC('DAY', j.EVENT_TS) AS METRIC_DATE,
    COUNT(DISTINCT j.VEHICLE_ID) AS ACTIVE_VEHICLES,
    COUNT(*) AS TOTAL_READINGS,
    ROUND(AVG(j.J1939_VEHICLE_SPEED_KPH), 1) AS FLEET_AVG_SPEED_KPH,
    ROUND(AVG(j.J1939_FUEL_RATE_LPH), 2) AS FLEET_AVG_FUEL_RATE_LPH,
    ROUND(SUM(COALESCE(j.J1939_TOTAL_FUEL_USED_L, 0)), 0) AS FLEET_TOTAL_FUEL_L,
    ROUND(AVG(j.J1939_ENGINE_LOAD_PCT), 1) AS FLEET_AVG_ENGINE_LOAD_PCT,
    ROUND(AVG(j.J1939_COOLANT_TEMP_C), 1) AS FLEET_AVG_COOLANT_TEMP_C,
    SUM(CASE WHEN j.J1939_ACTIVE_DTC_COUNT > 0 THEN 1 ELSE 0 END) AS READINGS_WITH_DTC,
    ROUND(AVG(j.J1939_BATTERY_VOLTAGE_V), 2) AS FLEET_AVG_BATTERY_V,
    SUM(CASE WHEN j.J1939_BRAKE_SWITCH = TRUE THEN 1 ELSE 0 END) AS FLEET_BRAKE_EVENTS,
    ROUND(AVG(j.J1939_TYRE_PRESSURE_FRONT_LEFT_KPA), 1) AS FLEET_AVG_TYRE_FL_KPA,
    ROUND(AVG(j.J1939_DEF_LEVEL_PCT), 1) AS FLEET_AVG_DEF_LEVEL_PCT
FROM TML_FLEET_TELEMETRY.CURATED.J1939_SIGNALS_V1 j
GROUP BY j.CUSTOMER_ID, DATE_TRUNC('DAY', j.EVENT_TS);

-- 7c. DIM_VEHICLE_V1
CREATE OR REPLACE DYNAMIC TABLE DIM_VEHICLE_V1
    TARGET_LAG = '5 minutes'
    WAREHOUSE = TML_HACKATHON
    REFRESH_MODE = AUTO
    INITIALIZE = ON_CREATE
AS
SELECT DISTINCT
    v.VEHICLE_ID, v.CUSTOMER_ID, v.DEVICE_ID,
    c.CUSTOMER_NAME, c.RAW_RETENTION_DAYS, c.CURATED_RETENTION_DAYS,
    c.TRIP_RETENTION_DAYS, c.TENANT_ISOLATION_ENABLED,
    MIN(v.EVENT_TS) AS FIRST_SEEN_TS,
    MAX(v.EVENT_TS) AS LAST_SEEN_TS,
    COUNT(*) AS TOTAL_GPS_EVENTS
FROM TML_FLEET_TELEMETRY.CURATED.GPS_SIGNALS_V1 v
LEFT JOIN TML_FLEET_TELEMETRY.MASTER.CUSTOMER_RETENTION_POLICIES c
    ON c.CUSTOMER_ID = v.CUSTOMER_ID
GROUP BY v.VEHICLE_ID, v.CUSTOMER_ID, v.DEVICE_ID,
    c.CUSTOMER_NAME, c.RAW_RETENTION_DAYS, c.CURATED_RETENTION_DAYS,
    c.TRIP_RETENTION_DAYS, c.TENANT_ISOLATION_ENABLED;

