-- STEP 10: Retention Enforcement — Stored Procedure and Task
-- ────────────────────────────────────────────────────────────────────────────
USE SCHEMA TML_FLEET_TELEMETRY.RAW;

CREATE OR REPLACE PROCEDURE ENFORCE_CUSTOMER_RETENTION()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS CALLER
AS
$$
DECLARE
    total_deleted INT DEFAULT 0;
    cust_deleted INT DEFAULT 0;
    cust_id VARCHAR;
    retention_days INT;
    cur CURSOR FOR
        SELECT CUSTOMER_ID, RAW_RETENTION_DAYS
        FROM TML_FLEET_TELEMETRY.MASTER.CUSTOMER_RETENTION_POLICIES;
BEGIN
    FOR rec IN cur DO
        cust_id := rec.CUSTOMER_ID;
        retention_days := rec.RAW_RETENTION_DAYS;

        DELETE FROM TML_FLEET_TELEMETRY.RAW.VEHICLE_TELEMETRY_RAW_V1
        WHERE CUSTOMER_ID = :cust_id
          AND EVENT_TS < DATEADD('DAY', -1 * :retention_days, CURRENT_TIMESTAMP());

        cust_deleted := SQLROWCOUNT;
        total_deleted := total_deleted + cust_deleted;
    END FOR;

    RETURN 'RAW retention enforced. Total rows deleted: ' || total_deleted::VARCHAR;
END;
$$;

