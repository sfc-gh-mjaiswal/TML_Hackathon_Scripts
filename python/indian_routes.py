GEOFENCE_CITIES = {}
for route in INDIAN_ROUTES:
    start_wp = route["waypoints"][0]
    end_wp = route["waypoints"][-1]
    GEOFENCE_CITIES[route["from"]] = {"lat": start_wp[0], "lon": start_wp[1], "radius_km": 15}
    GEOFENCE_CITIES[route["to"]] = {"lat": end_wp[0], "lon": end_wp[1], "radius_km": 15}

ROAD_SEGMENTS = []
_seg_id = 0
for route in INDIAN_ROUTES:
    wps = route["waypoints"]
    for i in range(len(wps) - 1):
        lat1, lon1 = wps[i]
        lat2, lon2 = wps[i + 1]
        mid_lat = (lat1 + lat2) / 2
        mid_lon = (lon1 + lon2) / 2
        dlat = math.radians(lat2 - lat1)
        dlon = math.radians(lon2 - lon1)
        a = math.sin(dlat / 2) ** 2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon / 2) ** 2
        seg_km = round(6371 * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a)), 1)
        ROAD_SEGMENTS.append({
            "segment_id": f"SEG_{_seg_id:04d}",
            "route_name": route["name"],
            "highway": route["highway"],
            "from_city": route["from"],
            "to_city": route["to"],
            "waypoint_idx": i,
            "start_lat": lat1, "start_lon": lon1,
            "end_lat": lat2, "end_lon": lon2,
            "mid_lat": mid_lat, "mid_lon": mid_lon,
            "segment_km": seg_km,
        })
        _seg_id += 1


def interpolate_waypoints(wp1, wp2, fraction):
    lat = wp1[0] + (wp2[0] - wp1[0]) * fraction
    lon = wp1[1] + (wp2[1] - wp1[1]) * fraction
    return (lat, lon)


def bearing_between(wp1, wp2):
    lat1, lon1 = math.radians(wp1[0]), math.radians(wp1[1])
    lat2, lon2 = math.radians(wp2[0]), math.radians(wp2[1])
    dlon = lon2 - lon1
    x = math.sin(dlon) * math.cos(lat2)
    y = math.cos(lat1) * math.sin(lat2) - math.sin(lat1) * math.cos(lat2) * math.cos(dlon)
    brng = math.degrees(math.atan2(x, y))
    return (brng + 360) % 360


def haversine_km(lat1, lon1, lat2, lon2):
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat / 2) ** 2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon / 2) ** 2
    return 6371 * 2 * math.atan2(math.sqrt(a), math.sqrt(1 - a))


def is_in_geofence(lat, lon, city_name, radius_km=None):
    if city_name not in GEOFENCE_CITIES:
        return False
    geo = GEOFENCE_CITIES[city_name]
    r = radius_km or geo["radius_km"]
    return haversine_km(lat, lon, geo["lat"], geo["lon"]) <= r
